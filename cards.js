const CATEGORIES = [...new Set(CARDS.map(c => c.category))];

// Get cards filtered by difficulty and categories
function getFilteredCards(difficulty, selectedCategories) {
  return CARDS.filter(card => {
    const diffMatch = !difficulty || card.difficulty === difficulty;
    const catMatch = !selectedCategories || selectedCategories.length === 0 || selectedCategories.includes(card.category);
    return diffMatch && catMatch;
  });
}

// Get a random card
function getRandomCard(difficulty, selectedCategories) {
  const filtered = getFilteredCards(difficulty, selectedCategories);
  if (filtered.length === 0) return null;
  return filtered[Math.floor(Math.random() * filtered.length)];
}

// Shuffle an array
function shuffleArray(arr) {
  const shuffled = [...arr];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}